class CompatRow(dict):
    """Dict row that also supports legacy sqlite-style integer indexing."""

    def __init__(self, data: dict[str, Any]):
        super().__init__(data)
        self._columns = list(data.keys())

    def __getitem__(self, key: Any) -> Any:
        if isinstance(key, int):
            return super().__getitem__(self._columns[key])
        return super().__getitem__(key)


class CompatCursor:
    def __init__(self, cursor: Any):
        self._cursor = cursor
        self.lastrowid: int | None = None

    @property
    def rowcount(self) -> int:
        return int(getattr(self._cursor, "rowcount", -1))

    def execute(self, query: str, params: Iterable[Any] | None = None):
        prepared, values, capture_id = _prepare_sql(query, params)
        self.lastrowid = None
        self._cursor.execute(prepared, values)
        if capture_id and self.rowcount > 0:
            row = self._cursor.fetchone()
            if row:
                self.lastrowid = int(row.get("id") if isinstance(row, dict) else row[0])
        return self

    def executemany(self, query: str, seq_of_params: Iterable[Iterable[Any]]):
        prepared, _values, _capture_id = _prepare_sql(query, None, capture_identity=False)
        self.lastrowid = None
        self._cursor.executemany(prepared, [_normalize_params(params) for params in seq_of_params])
        return self

    def fetchone(self):
        return _wrap_row(self._cursor.fetchone())

    def fetchall(self):
        return [_wrap_row(row) for row in self._cursor.fetchall()]

    def close(self) -> None:
        self._cursor.close()

    def __getattr__(self, name: str) -> Any:
        return getattr(self._cursor, name)


class CompatConnection:
    def __init__(self, conn: Any, pooled: bool = False, pool: Any = None):
        self._conn = conn
        self._pooled = pooled
        self._pool = pool
        self._released = False

    def cursor(self, *args: Any, **kwargs: Any) -> CompatCursor:
        return CompatCursor(self._conn.cursor(*args, **kwargs))

    def execute(self, query: str, params: Iterable[Any] | None = None) -> CompatCursor:
        cursor = self.cursor()
        cursor.execute(query, params)
        return cursor

    def commit(self) -> None:
        self._conn.commit()

    def rollback(self) -> None:
        self._conn.rollback()

    def close(self) -> None:
        # Idempotent: return pooled connections to the pool instead of closing
        # the socket; close plain (fallback) connections normally.
        if self._released:
            return
        self._released = True
        _release_connection(self._conn, self._pooled, self._pool)

    def __getattr__(self, name: str) -> Any:
        return getattr(self._conn, name)


def _wrap_row(row: Any):
    if row is None:
        return None
    if isinstance(row, CompatRow):
        return row
    if isinstance(row, dict):
        return CompatRow({key: _normalize_value(value) for key, value in dict(row).items()})
    return row


def _normalize_value(value: Any) -> Any:
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, date):
        return value.isoformat()
    return value


def _normalize_params(params: Iterable[Any] | None):
    if params is None:
        return None
    return tuple(1 if value is True else 0 if value is False else value for value in params)


def _prepare_sql(
    query: str,
    params: Iterable[Any] | None,
    *,
    capture_identity: bool = True,
) -> tuple[str, Iterable[Any] | None, bool]:
    text = str(query).strip()
    text = re.sub(
        r"INTEGER\s+PRIMARY\s+KEY\s+AUTOINCREMENT",
        "INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY",
        text,
        flags=re.IGNORECASE,
    )
    text = re.sub(
        r"\b(TEXT)\s+DEFAULT\s+CURRENT_TIMESTAMP\b",
        "TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP",
        text,
        flags=re.IGNORECASE,
    )
    text = re.sub(
        r"\bALTER\s+TABLE\s+([^\s]+)\s+ADD\s+COLUMN\s+(?!IF\s+NOT\s+EXISTS)",
        r"ALTER TABLE \1 ADD COLUMN IF NOT EXISTS ",
        text,
        flags=re.IGNORECASE,
    )
    text = re.sub(r"\bIS\s+\?", "IS NOT DISTINCT FROM ?", text, flags=re.IGNORECASE)
    text = re.sub(
        r"datetime\('now',\s*\?\)",
        "(CURRENT_TIMESTAMP + ?::interval)",
        text,
        flags=re.IGNORECASE,
    )

    insert_ignore = bool(re.match(r"INSERT\s+OR\s+IGNORE\s+INTO\b", text, re.IGNORECASE))
    if insert_ignore:
        text = re.sub(r"^INSERT\s+OR\s+IGNORE\s+INTO", "INSERT INTO", text, flags=re.IGNORECASE)
        if " ON CONFLICT " not in text.upper():
            text = text.rstrip(";") + " ON CONFLICT DO NOTHING"

    capture_id = False
    table = _insert_table(text)
    if capture_identity and table in _IDENTITY_TABLES and " RETURNING " not in text.upper():
        text = text.rstrip(";") + " RETURNING id"
        capture_id = True

    text = text.replace("?", "%s")
    return text, _normalize_params(params), capture_id


def _insert_table(query: str) -> str | None:
    match = re.match(r"INSERT\s+INTO\s+([A-Za-z_][A-Za-z0-9_]*)\b", query.strip(), re.IGNORECASE)
    return match.group(1).lower() if match else None


def _database_exists(settings: dict[str, Any]) -> bool:
    maintenance = str(settings.get("maintenance_database") or "postgres")
    conn = psycopg2.connect(
        host=settings["host"],
        port=settings["port"],
        dbname=maintenance,
        user=settings["user"],
        password=settings["password"],
        cursor_factory=RealDictCursor,
    )
    try:
        conn.autocommit = True
        with conn.cursor() as cursor:
            cursor.execute("SELECT 1 FROM pg_database WHERE datname = %s", (settings["database"],))
            return cursor.fetchone() is not None
    finally:
        conn.close()


def ensure_database() -> None:
    settings = config.javhub_database
    if _database_exists(settings):
        return
    maintenance = str(settings.get("maintenance_database") or "postgres")
    conn = psycopg2.connect(
        host=settings["host"],
        port=settings["port"],
        dbname=maintenance,
        user=settings["user"],
        password=settings["password"],
        cursor_factory=RealDictCursor,
    )
    try:
        conn.autocommit = True
        with conn.cursor() as cursor:
            cursor.execute(
                sql.SQL("CREATE DATABASE {}").format(sql.Identifier(settings["database"]))
            )
    finally:
        conn.close()


# === Connection pool ===========================================================
# Every DB call used to open a brand-new psycopg2 connection and tear it down
# afterwards. Across a compare/process run that is thousands of TCP+auth
# round-trips that block whatever loop/thread is driving the job. A process-wide
# ThreadedConnectionPool reuses connections instead. Pooling is best-effort: if
# the pool can't be built or is momentarily exhausted, we transparently fall
# back to a direct connection so the app never hard-fails on a pool error.

_pool: _pg_pool.ThreadedConnectionPool | None = None
_pool_key: tuple | None = None
_pool_lock = threading.Lock()


def _settings_key(settings: dict) -> tuple:
    return (settings["host"], settings["port"], settings["database"], settings["user"])


def _connect_direct() -> Any:
    """Open a single raw psycopg2 connection (creating the DB on first use)."""
    settings = config.javhub_database
    kwargs = dict(
        host=settings["host"],
        port=settings["port"],
        dbname=settings["database"],
        user=settings["user"],
        password=settings["password"],
        cursor_factory=RealDictCursor,
    )
    try:
        return psycopg2.connect(**kwargs)
    except psycopg2.OperationalError as exc:
        if f'database "{settings["database"]}" does not exist' not in str(exc):
            raise
        ensure_database()
        return psycopg2.connect(**kwargs)


def _create_pool() -> _pg_pool.ThreadedConnectionPool:
    settings = config.javhub_database
    maxconn = int(settings.get("pool_max") or 32)
    kwargs = dict(
        host=settings["host"],
        port=settings["port"],
        dbname=settings["database"],
        user=settings["user"],
        password=settings["password"],
        cursor_factory=RealDictCursor,
    )
    try:
        return _pg_pool.ThreadedConnectionPool(1, maxconn, **kwargs)
    except psycopg2.OperationalError as exc:
        if f'database "{settings["database"]}" does not exist' not in str(exc):
            raise
        ensure_database()
        return _pg_pool.ThreadedConnectionPool(1, maxconn, **kwargs)


def _get_pool() -> _pg_pool.ThreadedConnectionPool | None:
    """Return the pool for the *current* DB settings, rebuilding on change.

    Keying by settings keeps test isolation intact: each test repoints config at
    a fresh temp database via ``config.reload()``, and a stale pool bound to a
    previous (possibly dropped) database must not keep serving connections.
    """
    global _pool, _pool_key
    key = _settings_key(config.javhub_database)
    if _pool is not None and _pool_key == key:
        return _pool
    with _pool_lock:
        if _pool is not None and _pool_key == key:
            return _pool
        if _pool is not None:
            try:
                _pool.closeall()
            except Exception:
                pass
            _pool = None
            _pool_key = None
        try:
            _pool = _create_pool()
            _pool_key = key
        except Exception:
            _pool = None  # pooling unavailable → direct connections
            _pool_key = None
    return _pool


def _release_connection(raw: Any, pooled: bool, pool: Any = None) -> None:
    if pooled and pool is not None:
        try:
            if not getattr(raw, "closed", False):
                # Reset any open/aborted transaction before reuse so the next
                # borrower never inherits a dirty session.
                raw.rollback()
            pool.putconn(raw)
            return
        except Exception:
            try:
                raw.close()
            except Exception:
                pass
            return
    try:
        raw.close()
    except Exception:
        pass


def get_db_orig():
    """Return a PostgreSQL connection wrapper for JavHub state.

    Borrows from the shared pool when available; falls back to a fresh direct
    connection if the pool is unavailable or exhausted.
    """
    pool = _get_pool()
    if pool is not None:
        try:
            return CompatConnection(pool.getconn(), pooled=True, pool=pool)
        except _pg_pool.PoolError:
            pass  # exhausted/closed → degrade gracefully to a direct connection
    return CompatConnection(_connect_direct(), pooled=False)


@contextmanager
def get_db():
    """Database connection context manager with commit/rollback/close."""
    conn = get_db_orig()
    try:
        yield conn
        conn.commit()
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()


