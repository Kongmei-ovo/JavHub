def _init_library_tables(cursor) -> None:
    """自有云盘文件索引 + 播放进度（去 Emby 化，见 docs/superpowers/plans/2026-06-12-library-index-player.md）。

    约束：library_files 只存引用（backend+path+ref_payload），任何直链都不允许入库。
    """
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS library_files (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            backend TEXT NOT NULL DEFAULT 'openlist',
            path TEXT NOT NULL,
            name TEXT NOT NULL,
            size BIGINT DEFAULT 0,
            modified_at TEXT,
            content_id TEXT,
            match_status TEXT DEFAULT 'unmatched',
            ref_payload TEXT,
            first_seen_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
            last_seen_at TIMESTAMPTZ,
            deleted_at TIMESTAMPTZ,
            UNIQUE(backend, path)
        )
    ''')

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS library_scan_runs (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            mode TEXT NOT NULL,
            root_path TEXT,
            status TEXT DEFAULT 'running',
            files_seen INTEGER DEFAULT 0,
            files_added INTEGER DEFAULT 0,
            files_removed INTEGER DEFAULT 0,
            files_matched INTEGER DEFAULT 0,
            error TEXT,
            started_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
            finished_at TIMESTAMPTZ
        )
    ''')

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS playback_progress (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            content_id TEXT NOT NULL,
            user_id TEXT,
            source TEXT NOT NULL DEFAULT 'library',
            position_seconds DOUBLE PRECISION DEFAULT 0,
            duration_seconds DOUBLE PRECISION DEFAULT 0,
            completed INTEGER DEFAULT 0,
            updated_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
            UNIQUE(content_id, source)
        )
    ''')


def _init_movie_resource_tables(cursor) -> None:
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS movie_resources (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            movie_id TEXT NOT NULL,
            provider TEXT NOT NULL DEFAULT 'open115',
            remote_file_id TEXT NOT NULL,
            parent_id TEXT,
            pick_code TEXT,
            name TEXT NOT NULL,
            extension TEXT,
            size BIGINT DEFAULT 0,
            duration BIGINT DEFAULT 0,
            resource_type TEXT NOT NULL DEFAULT 'video',
            status TEXT NOT NULL DEFAULT 'pending',
            is_default INTEGER NOT NULL DEFAULT 0,
            download_task_id INTEGER,
            related_resource_id INTEGER,
            created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
            updated_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
            UNIQUE(provider, remote_file_id),
            CHECK (resource_type IN ('video', 'subtitle')),
            CHECK (status IN ('pending', 'ready', 'missing', 'failed'))
        )
    ''')


